// Manipulating text with jQuery


$('h1').text('Bye');  // this will directly changes the text from Hello to Bye...

$('button').text("Don't click me");  // all the text of the button changes
$('button').text("<em>Hey</em>");  // all the text of the button changes

$('button').html("<em>Hey</em>");   // you can see the font of the text inside the button changes